            (function($){
                // full page 사용
                $(document).ready(function(){
                    $('#fullpage').fullpage({
                        autoScrolling: true,
                        scrollHorizontally: true
                    });
                });
                
                // nav event 
                $(document).on('click', '#header > .btn-nav', function(){
                    $('body').addClass('opened');
                });
                $(document).on('click', '#nav > .backdrop, #nav .nav-close', function(){
                    $('body').removeClass('opened');
                });
                
                // gnb menu event
                $(document).on('click', '.gnb-menu > ul > li > a', function() {
                    if ($(this).parent().hasClass('active')) {
                        $(this).parent().removeClass('active');
                    } else {
                        $(this).parent().siblings('.active').removeClass('active');
                        $(this).parent().addClass('active');
                    }
                });
                
                // location-guide event
                $(document).on('mouseenter focus', '.guide-list > li > a', function() {
                    $(this).parent().siblings().removeClass('active');
                    $(this).parent().addClass('active');
                });
                $(document).on('mouseleave', '.guide-list', function() {
                    $('.guide-list > li').removeClass('active');
                });
                
                $(document).on("click", ".moveBtn", function(){
                    var lang = $(this).attr("data-type");
                    var event = $.Event("mousewheel");
                    event.deltaY = -1;
                    $("body").trigger("event");
                })
            })(jQuery);
